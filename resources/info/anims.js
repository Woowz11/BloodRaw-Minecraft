function ADDA(Name,x,y,w,h,Extra,AnimProperties){
	if(Name==""){return;}
	if(Extra==null){Extra = [];}
	if(x!=null&&y!=null){if(w==null){w=1;}if(h==null){h=1;}}
	var Result = {
		"Name": Name,
		"Extra": Extra,
		"Texture": {
			"Path": Path+SelectedTexture+".png",
			"x": x*16,
			"y": y*16,
			"w": 1,
			"h": h*16
		},
		"Animation": {
			"Speed" : AnimProperties["speed" ],
			"Smooth": AnimProperties["smooth"],
			"Frames": AnimProperties["frames"],
		}
	};
	RPI[Name] = Result;
}

